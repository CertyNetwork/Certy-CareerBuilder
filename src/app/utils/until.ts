import { toast } from 'react-toastify';

export const handleErrorResponse = error => {
  console.log(error.message, 111);
  const mes = error?.message;
  return toast.error(mes, {
    position: 'bottom-right',
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: 'colored',
  });
};

